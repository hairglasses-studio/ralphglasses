package themekit

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"
)

// SyncConfig controls which external tools receive theme exports.
type SyncConfig struct {
	Alacritty bool
	Kitty     bool
	Tmux      bool
	Starship  bool
	Bat       bool
}

// ExportResult captures the outcome of exporting a theme to a single tool.
type ExportResult struct {
	Tool    string
	Path    string
	Content string
	Applied bool
	Error   error
}

// ExportAll generates and writes config snippets for every enabled tool in cfg.
// It always attempts all enabled tools, collecting results even on individual failures.
func ExportAll(palette Palette, cfg SyncConfig) ([]ExportResult, error) {
	type exporter struct {
		name string
		fn   func(Palette) (string, string, error) // returns (path, content, err)
	}

	var targets []exporter
	if cfg.Alacritty {
		targets = append(targets, exporter{"alacritty", exportAlacritty})
	}
	if cfg.Kitty {
		targets = append(targets, exporter{"kitty", exportKitty})
	}
	if cfg.Tmux {
		targets = append(targets, exporter{"tmux", exportTmux})
	}
	if cfg.Starship {
		targets = append(targets, exporter{"starship", func(p Palette) (string, string, error) {
			path, err := ExportStarship(p)
			if err != nil {
				return "", "", err
			}
			data, err := os.ReadFile(path)
			if err != nil {
				return path, "", err
			}
			return path, string(data), nil
		}})
	}
	if cfg.Bat {
		targets = append(targets, exporter{"bat", func(p Palette) (string, string, error) {
			path, err := ExportBat(p)
			if err != nil {
				return "", "", err
			}
			data, err := os.ReadFile(path)
			if err != nil {
				return path, "", err
			}
			return path, string(data), nil
		}})
	}

	results := make([]ExportResult, 0, len(targets))
	var firstErr error

	for _, t := range targets {
		path, content, err := t.fn(palette)
		r := ExportResult{
			Tool:    t.name,
			Path:    path,
			Content: content,
			Applied: err == nil,
			Error:   err,
		}
		results = append(results, r)
		if err != nil && firstErr == nil {
			firstErr = fmt.Errorf("export %s: %w", t.name, err)
		}
	}

	return results, firstErr
}

// exportAlacritty generates an Alacritty TOML color theme and writes it to
// ~/.config/alacritty/catppuccin.toml.
func exportAlacritty(p Palette) (string, string, error) {
	home, err := os.UserHomeDir()
	if err != nil {
		return "", "", err
	}

	configDir := filepath.Join(home, ".config", "alacritty")
	if err := os.MkdirAll(configDir, 0o755); err != nil {
		return "", "", fmt.Errorf("create alacritty config dir: %w", err)
	}

	content := generateAlacrittyTOML(p)
	outPath := filepath.Join(configDir, "catppuccin.toml")

	if err := os.WriteFile(outPath, []byte(content), 0o644); err != nil {
		return "", "", fmt.Errorf("write alacritty theme: %w", err)
	}

	return outPath, content, nil
}

