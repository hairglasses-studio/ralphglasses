func generateTmuxConf(p Palette) string {
	hex := func(name string) string {
		return "#" + p.Get(name).Hex
	}

	var b strings.Builder
	b.WriteString(fmt.Sprintf("# %s tmux theme (generated by ralphglasses)\n", p.Name))
	b.WriteString("# Source in tmux.conf: source-file ~/.config/tmux/catppuccin.conf\n\n")

	// Status bar
	b.WriteString("# Status bar\n")
	b.WriteString(fmt.Sprintf("set -g status-style \"bg=%s,fg=%s\"\n", hex("mantle"), hex("text")))
	b.WriteString(fmt.Sprintf("set -g status-left-style \"bg=%s,fg=%s,bold\"\n", hex("blue"), hex("crust")))
	b.WriteString(fmt.Sprintf("set -g status-right-style \"bg=%s,fg=%s\"\n", hex("surface0"), hex("text")))
	b.WriteString("\n")

	// Window status
	b.WriteString("# Window status\n")
	b.WriteString(fmt.Sprintf("set -g window-status-style \"bg=%s,fg=%s\"\n", hex("mantle"), hex("text")))
	b.WriteString(fmt.Sprintf("set -g window-status-current-style \"bg=%s,fg=%s,bold\"\n", hex("surface0"), hex("text")))
	b.WriteString(fmt.Sprintf("set -g window-status-activity-style \"bg=%s,fg=%s\"\n", hex("mantle"), hex("yellow")))
	b.WriteString("\n")

	// Pane borders
	b.WriteString("# Pane borders\n")
	b.WriteString(fmt.Sprintf("set -g pane-border-style \"fg=%s\"\n", hex("surface1")))
	b.WriteString(fmt.Sprintf("set -g pane-active-border-style \"fg=%s\"\n", hex("lavender")))
	b.WriteString("\n")

	// Messages
	b.WriteString("# Messages\n")
	b.WriteString(fmt.Sprintf("set -g message-style \"bg=%s,fg=%s\"\n", hex("surface0"), hex("text")))
	b.WriteString(fmt.Sprintf("set -g message-command-style \"bg=%s,fg=%s\"\n", hex("surface0"), hex("peach")))
	b.WriteString("\n")

	// Mode (copy mode, etc.)
	b.WriteString("# Mode\n")
	b.WriteString(fmt.Sprintf("set -g mode-style \"bg=%s,fg=%s\"\n", hex("surface0"), hex("text")))
	b.WriteString("\n")

	// Clock
	b.WriteString("# Clock\n")
	b.WriteString(fmt.Sprintf("set -g clock-mode-colour \"%s\"\n", hex("blue")))

	return b.String()
}
