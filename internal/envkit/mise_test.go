func TestMiseInstallWithFakeMise(t *testing.T) {
	fakeDir := createFakeMise(t)
	t.Setenv("PATH", fakeDir)
	workDir := t.TempDir()
	// Change to work dir so GenerateMiseConfig writes there
	origDir, _ := os.Getwd()
	if err := os.Chdir(workDir); err != nil {
		t.Fatal(err)
	}
	defer os.Chdir(origDir)

	ctx := context.Background()
	result, err := MiseInstall(ctx, MiseOpts{
		Tools: map[string]string{"go": "1.23"},
	})
	if err != nil {
		t.Fatalf("MiseInstall with fake mise: %v", err)
	}
	if result == nil {
		t.Fatal("expected non-nil result")
	}
	if result.ConfigPath == "" {
		t.Error("expected non-empty ConfigPath")
	}
	// Verify config was written
	if _, err := os.Stat(result.ConfigPath); err != nil {
		t.Errorf("config file should exist: %v", err)
	}
}

