func TestGenerateMiseConfigFormat(t *testing.T) {
	dir := t.TempDir()
	tools := map[string]string{
		"go":     "1.23",
		"node":   "lts",
		"python": "3.12",
	}

	path, err := GenerateMiseConfig(dir, tools)
	if err != nil {
		t.Fatalf("GenerateMiseConfig: %v", err)
	}

	data, err := os.ReadFile(path)
	if err != nil {
		t.Fatalf("read config: %v", err)
	}

	content := string(data)

	// Verify header comment
	if !strings.HasPrefix(content, "# Generated by claudekit") {
		t.Error("config should start with generated-by comment")
	}

	// Verify [tools] section
	if !strings.Contains(content, "[tools]") {
		t.Error("config should contain [tools] section")
	}

	// Verify each tool is present with quoted version
	for name, version := range tools {
		expected := name + " = \"" + version + "\""
		if !strings.Contains(content, expected) {
			t.Errorf("config should contain %q, got:\n%s", expected, content)
		}
	}
}

