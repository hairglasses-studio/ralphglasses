func TestCalculateTimeout(t *testing.T) {
	converter := NewPatternConverter(nil, "")

	tests := []struct {
		toolCount int
		expected  string
	}{
		{toolCount: 1, expected: "1m"},
		{toolCount: 2, expected: "1m"},
		{toolCount: 3, expected: "2m"},
		{toolCount: 5, expected: "4m"}, // 5 * 30s = 150s -> 2.5min * 1.5 = 3.75 -> 4m
		{toolCount: 8, expected: "6m"}, // 8 * 30s = 240s = 4min * 1.5 = 6min
	}

	for _, tt := range tests {
		pattern := patterns.DiscoveredPattern{
			ToolSequence: make([]string, tt.toolCount),
		}
		for i := range pattern.ToolSequence {
			pattern.ToolSequence[i] = "webb_test_tool"
		}

		timeout := converter.calculateTimeout(pattern)
		if timeout != tt.expected {
			t.Errorf("calculateTimeout(%d tools) = %s, expected %s", tt.toolCount, timeout, tt.expected)
		}
	}
}

func TestListConvertiblePatterns(t *testing.T) {
	converter := NewPatternConverter(nil, "")

	discoveredPatterns := []patterns.DiscoveredPattern{
		{
			ID:           "p1",
			Type:         patterns.PatternSequential,
			ToolSequence: []string{"webb_a", "webb_b"},
			Frequency:    5,
		},
		{
			ID:           "p2",
			Type:         patterns.PatternSequential,
			ToolSequence: []string{"webb_c"},
			Frequency:    10, // Only 1 tool - should be excluded
		},
		{
			ID:           "p3",
			Type:         patterns.PatternSequential,
			ToolSequence: []string{"webb_d", "webb_e", "webb_f"},
			Frequency:    2, // Below threshold
		},
		{
			ID:           "p4",
			Type:         "concurrent", // Wrong type
			ToolSequence: []string{"webb_g", "webb_h"},
			Frequency:    10,
		},
	}

	convertible := converter.ListConvertiblePatterns(discoveredPatterns, 3)

	if len(convertible) != 1 {
		t.Errorf("expected 1 convertible pattern, got %d", len(convertible))
	}

	if len(convertible) > 0 && convertible[0].ID != "p1" {
		t.Errorf("expected pattern p1, got %s", convertible[0].ID)
	}
}

