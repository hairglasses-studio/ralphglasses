func TestConvertPattern(t *testing.T) {
	registry := NewRegistry()
	converter := NewPatternConverter(registry, "")

	pattern := patterns.DiscoveredPattern{
		ID:           "test-pattern-001",
		Name:         "Investigation Flow",
		Type:         patterns.PatternSequential,
		ToolSequence: []string{"webb_k8s_pods", "webb_k8s_logs", "webb_grafana_alerts"},
		Frequency:    10,
		SuccessRate:  0.85,
	}

	chain := converter.ConvertPattern(pattern, "")

	if chain == nil {
		t.Fatal("expected non-nil chain")
	}

	if !strings.HasPrefix(chain.Name, "pattern-") {
		t.Errorf("expected chain name to start with 'pattern-', got %s", chain.Name)
	}

	if len(chain.Steps) != 3 {
		t.Errorf("expected 3 steps, got %d", len(chain.Steps))
	}

	if chain.Steps[0].Tool != "webb_k8s_pods" {
		t.Errorf("expected first step tool to be webb_k8s_pods, got %s", chain.Steps[0].Tool)
	}

	// Check tags
	hasAutoGenerated := false
	hasPatternMined := false
	for _, tag := range chain.Tags {
		if tag == "auto-generated" {
			hasAutoGenerated = true
		}
		if tag == "pattern-mined" {
			hasPatternMined = true
		}
	}
	if !hasAutoGenerated {
		t.Error("expected 'auto-generated' tag")
	}
	if !hasPatternMined {
		t.Error("expected 'pattern-mined' tag")
	}
}

