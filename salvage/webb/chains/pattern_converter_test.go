func TestSaveAsYAML(t *testing.T) {
	tmpDir := t.TempDir()
	registry := NewRegistry()
	converter := NewPatternConverter(registry, tmpDir)

	pattern := patterns.DiscoveredPattern{
		ID:           "yaml-test-001",
		Name:         "Ticket Investigation",
		Type:         patterns.PatternSequential,
		ToolSequence: []string{"webb_pylon_search", "webb_incident_list"},
		Frequency:    8,
		SuccessRate:  0.75,
	}

	result, err := converter.SaveAsYAML(pattern, "")
	if err != nil {
		t.Fatalf("failed to save as YAML: %v", err)
	}

	if result.SavedPath == "" {
		t.Error("expected non-empty saved path")
	}

	// Check file exists
	if _, err := os.Stat(result.SavedPath); os.IsNotExist(err) {
		t.Errorf("expected file at %s", result.SavedPath)
	}

	// Read and verify contents
	data, err := os.ReadFile(result.SavedPath)
	if err != nil {
		t.Fatalf("failed to read saved file: %v", err)
	}

	content := string(data)
	if !strings.Contains(content, "Auto-generated chain from discovered pattern") {
		t.Error("expected header comment in YAML file")
	}
	if !strings.Contains(content, "webb_pylon_search") {
		t.Error("expected tool name in YAML file")
	}
}

func TestRegisterChain(t *testing.T) {
	registry := NewRegistry()
	converter := NewPatternConverter(registry, "")

	pattern := patterns.DiscoveredPattern{
		ID:           "register-test-001",
		Name:         "Alert Response",
		Type:         patterns.PatternSequential,
		ToolSequence: []string{"webb_grafana_alerts", "webb_k8s_pods"},
		Frequency:    15,
		SuccessRate:  0.95,
	}

	result, err := converter.RegisterChain(pattern, "alert-response-chain")
	if err != nil {
		t.Fatalf("failed to register chain: %v", err)
	}

	if !result.Registered {
		t.Error("expected Registered to be true")
	}

	if result.ChainName != "alert-response-chain" {
		t.Errorf("expected chain name 'alert-response-chain', got %s", result.ChainName)
	}

	// Verify chain is in registry
	chain, err := registry.Get("alert-response-chain")
	if err != nil {
		t.Fatalf("failed to get registered chain: %v", err)
	}

	if chain.Name != "alert-response-chain" {
		t.Errorf("expected registered chain name, got %s", chain.Name)
	}
}

