package gen

import (
	"bytes"
	"fmt"
	"os"
	"path/filepath"
	"strings"
	"text/template"

	"gopkg.in/yaml.v3"
)

// Generator generates Go code from YAML tool definitions
type Generator struct {
	tmpl *template.Template
}

// NewGenerator creates a new code generator
func NewGenerator() (*Generator, error) {
	tmpl, err := template.New("module").Funcs(template.FuncMap{
		"complexity":   ComplexityFromString,
		"mcpMethod":    TypeToMCPMethod,
		"quote":        func(s string) string { return fmt.Sprintf("%q", s) },
		"join":         strings.Join,
		"stringSlice":  formatStringSlice,
		"lower":        strings.ToLower,
		"hasParams":    func(params []ParamDefinition) bool { return len(params) > 0 },
		"hasRequired":  hasRequiredParams,
		"hasOptional":  hasOptionalParams,
		"requiredOnly": filterRequired,
		"optionalOnly": filterOptional,
	}).Parse(moduleTemplate)
	if err != nil {
		return nil, fmt.Errorf("failed to parse template: %w", err)
	}
	return &Generator{tmpl: tmpl}, nil
}

// GenerateFromFile reads a YAML file and generates Go code
func (g *Generator) GenerateFromFile(yamlPath string) ([]byte, error) {
	data, err := os.ReadFile(yamlPath)
	if err != nil {
		return nil, fmt.Errorf("failed to read YAML file: %w", err)
	}

	var module ModuleDefinition
	if err := yaml.Unmarshal(data, &module); err != nil {
		return nil, fmt.Errorf("failed to parse YAML: %w", err)
	}

	return g.Generate(&module)
}

// Generate produces Go code from a ModuleDefinition
func (g *Generator) Generate(module *ModuleDefinition) ([]byte, error) {
	var buf bytes.Buffer
	if err := g.tmpl.Execute(&buf, module); err != nil {
		return nil, fmt.Errorf("failed to execute template: %w", err)
	}
	return buf.Bytes(), nil
}

// GenerateToFile generates Go code and writes it to a file
func (g *Generator) GenerateToFile(yamlPath, outputPath string) error {
	code, err := g.GenerateFromFile(yamlPath)
	if err != nil {
		return err
	}

	// Ensure output directory exists
	dir := filepath.Dir(outputPath)
	if err := os.MkdirAll(dir, 0755); err != nil {
		return fmt.Errorf("failed to create output directory: %w", err)
	}

	if err := os.WriteFile(outputPath, code, 0644); err != nil {
		return fmt.Errorf("failed to write output file: %w", err)
	}

	return nil
}

// Helper functions for template

func formatStringSlice(items []string) string {
	if len(items) == 0 {
		return "nil"
	}
	quoted := make([]string, len(items))
	for i, item := range items {
		quoted[i] = fmt.Sprintf("%q", item)
	}
	return "[]string{" + strings.Join(quoted, ", ") + "}"
}

func hasRequiredParams(params []ParamDefinition) bool {
	for _, p := range params {
		if p.Required {
			return true
		}
	}
	return false
}

func hasOptionalParams(params []ParamDefinition) bool {
	for _, p := range params {
		if !p.Required {
			return true
		}
	}
	return false
}

func filterRequired(params []ParamDefinition) []ParamDefinition {
	var result []ParamDefinition
	for _, p := range params {
		if p.Required {
			result = append(result, p)
		}
	}
	return result
}

func filterOptional(params []ParamDefinition) []ParamDefinition {
	var result []ParamDefinition
	for _, p := range params {
		if !p.Required {
			result = append(result, p)
		}
	}
	return result
}

// moduleTemplate is the Go code template for generating modules
const moduleTemplate = `// Code generated by webb toolgen. DO NOT EDIT.
// Source: tools.yaml

package {{.Package}}

import (
	"github.com/mark3labs/mcp-go/mcp"
	"github.com/hairglasses-studio/webb/internal/mcp/tools"
)

// Module implements the {{.Name}} module
type Module struct{}

func (m *Module) Name() string {
	return {{quote .Name}}
}

func (m *Module) Description() string {
	return {{quote .Description}}
}

func (m *Module) Tools() []tools.ToolDefinition {
	return []tools.ToolDefinition{
{{- range .Tools}}
		// {{.Name}}
		{
			Tool: mcp.NewTool({{quote .Name}},
				mcp.WithDescription({{quote .Description}}),
{{- range .Parameters}}
{{- if .Required}}
				{{mcpMethod .Type}}({{quote .Name}},
					mcp.Required(),
					mcp.Description({{quote .Description}}),
				),
{{- else}}
				{{mcpMethod .Type}}({{quote .Name}},
					mcp.Description({{quote .Description}}),
				),
{{- end}}
{{- end}}
			),
			Handler:     {{.Handler}},
			Category:    {{quote .Category}},
{{- if .Subcategory}}
			Subcategory: {{quote .Subcategory}},
{{- end}}
{{- if .Tags}}
			Tags:        {{stringSlice .Tags}},
{{- end}}
{{- if .UseCases}}
			UseCases:    {{stringSlice .UseCases}},
{{- end}}
			Complexity:  {{complexity .Complexity}},
{{- if .IsWrite}}
			IsWrite:     true,
{{- end}}
		},
{{- end}}
	}
}

func init() {
	tools.GetRegistry().RegisterModule(&Module{})
}
`
